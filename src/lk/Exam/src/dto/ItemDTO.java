public class ItemDTO {

    private String Id;
    private String name;
    private String qty;

    public ItemDTO() {
    }

    public ItemDTO(String id, String name, String qty) {
        Id = id;
        this.name = name;
        this.qty = qty;
    }

    public String getId() {
        return Id;
    }

    public void setId(String id) {
        Id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getQty() {
        return qty;
    }

    public void setQty(String qty) {
        this.qty = qty;
    }
}
